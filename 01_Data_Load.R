# 01_数据导入与预处理
# 准备开始处理最新的肿瘤切片 Xenium 数据
# ==============================================================================
# Xenium 空间转录组分析流程 - 步骤一：数据导入与对象构建模板
# ==============================================================================

# 1. 加载必要的工具包
library(Seurat)
library(dplyr)  # 用于数据表格处理

# ==============================================================================
# 2. 参数配置区 (🔴 换标本时，只修改这里)
# ==============================================================================
# 数据文件夹路径 (确保该文件夹下有 cell_feature_matrix.h5 等文件)
DATA_DIR <- "你的/数据/路径/Xenium_Sample_1" 

# 临床信息标签 (极其重要！为后续多样本合并做准备)
SAMPLE_ID    <- "Patient_01_Tumor"   # 样本编号
TISSUE_TYPE  <- "Tumor_Core"         # 组织类型 (如 Tumor, Normal)
TREATMENT    <- "Pre_Treatment"      # 治疗状态

# 是否读取亚细胞级 RNA 分子坐标？
# (⚠️ 警告：设为 TRUE 会消耗巨大内存，普通分析设为 FALSE 即可)
LOAD_MOLECULES <- FALSE 

# ==============================================================================
# 3. 核心执行区 (🟢 尽量不要修改下面的逻辑)
# ==============================================================================

print(paste("开始处理样本:", SAMPLE_ID))

# 3.1 动态构建读取逻辑
if (LOAD_MOLECULES == TRUE) {
  # 情况A：全量读取（含分子坐标）
  print("正在加载细胞矩阵和亚细胞分子坐标，请注意内存消耗...")
  seurat_obj <- LoadXenium(
    data.dir = DATA_DIR, 
    fov = "fov", 
    mols.dir = DATA_DIR # 告诉 Seurat 分子文件也在这个目录
  )
} else {
  # 情况B：轻量化读取（仅细胞矩阵和细胞中心坐标）
  print("正在进行轻量化加载...")
  seurat_obj <- LoadXenium(
    data.dir = DATA_DIR, 
    fov = "fov"
  )
}

# 3.2 强行注入临床与实验信息 (Meta.data)
# 这一步是很多新手会漏掉的。现在加上，以后合并数据时就会无比丝滑
seurat_obj$orig.ident   <- SAMPLE_ID
seurat_obj$tissue_type  <- TISSUE_TYPE
seurat_obj$treatment    <- TREATMENT

# 3.3 数据导入质量初步检查
print("数据导入完成！基础信息如下：")
print(seurat_obj) # 打印对象概要 (会显示多少基因，多少细胞)

# 检查空间模块是否挂载成功
if ("fov" %in% names(seurat_obj@images)) {
  print("✅ 空间坐标 (FOV) 加载成功！")
} else {
  warning("❌ 警告：未检测到空间坐标，请检查文件结构！")
}

# ==============================================================================
# 模板结束
# ==============================================================================