# ==========================================
# 第二步：质量控制 (Quality Control, QC)
# 核心目的：剔除“假细胞”（碎片、死细胞、双细胞），保留高质量细胞
# ==========================================

# ------------------------------------------
# 1. 探查病情：可视化当前数据的整体分布
# ------------------------------------------
# nCount_Xenium: 每个细胞里检测到的总 RNA 分子数（表达丰度）
# nFeature_Xenium: 每个细胞里检测到的不同基因的种类数（基因多样性）

# 画出小提琴图（Violin Plot），看看大部分细胞的表达量集中在什么范围
VlnPlot(xenium.obj, 
        features = c("nFeature_Xenium", "nCount_Xenium"), 
        ncol = 2, 
        pt.size = 0.1) + 
  theme(legend.position = "none") # 隐藏图例，让图面更干净

# 空间定位探查（可选但强烈推荐）
# 在肿瘤切片的真实物理二维空间上，看看那些表达量极低的点分布在哪里。
# 如果低表达的点都集中在切片边缘，那很可能就是切片时的物理性边缘破损。
SpatialFeaturePlot(xenium.obj, features = "nCount_Xenium")


# ------------------------------------------
# 2. 制定方案：设定过滤阈值 (Thresholds)
# ------------------------------------------
# 注意：以下数字（15, 500, 20）只是通用模板的示例！
# 在真实操作中，必须根据你上面画出的小提琴图的形态，来动态调整这几个数字。

min_features <- 15   # 最小基因数下限（低于这个值的，当作细胞碎片“切除”）
max_features <- 500  # 最大基因数上限（高于这个值的，当作双细胞或多细胞“切除”）
min_counts <- 20     # 最小RNA总分子数下限（剔除几乎没有活性的死点）


# ------------------------------------------
# 3. 执行切除：使用 subset 函数正式过滤
# ------------------------------------------
# 只有同时满足下面三个条件的细胞，才会被保留下来
xenium.obj <- subset(xenium.obj, 
                     subset = nFeature_Xenium > min_features & 
                       nFeature_Xenium < max_features & 
                       nCount_Xenium > min_counts)


# ------------------------------------------
# 4. 术后复查：再次画图确认
# ------------------------------------------
# 重新画一次小提琴图，看看极端的高值和低值是不是已经被平滑地去掉了
VlnPlot(xenium.obj, 
        features = c("nFeature_Xenium", "nCount_Xenium"), 
        ncol = 2, 
        pt.size = 0.1) + 
  theme(legend.position = "none")

# 打印一下现在还剩多少个高质量细胞，心里有个数
print(xenium.obj)
