# ==============================================================================
# 第二步：质量控制 (Quality Control, QC)
# 核心目的：剔除“假细胞”（碎片、死细胞、双细胞），保留高质量细胞
# 适用场景：Xenium 空间转录组大数据量（如几十万级别细胞）
# ==============================================================================

# ------------------------------------------------------------------------------
# 1. 探查病情：可视化当前数据的整体分布
# ------------------------------------------------------------------------------
# nCount_xenium: 每个细胞里检测到的总 RNA 分子数（表达丰度）
# nFeature_xenium: 每个细胞里检测到的不同基因的种类数（基因多样性）

# 画出小提琴图（Violin Plot）
# 【改良 1】：pt.size 必须设为 0！几十万个细胞画点不仅会变成“黑墙”遮挡视线，还会直接干满内存。
VlnPlot(seurat_obj, 
        features = c("nFeature_xenium", "nCount_xenium"), # 【改良 2】：统一使用小写 xenium，避免找不到变量的 Error
        ncol = 2, 
        pt.size = 0) + 
  theme(legend.position = "none") +
  ggtitle("QC分布图：请根据此图制定手术切除范围")

# 空间定位探查（可选但强烈推荐）
# 在肿瘤切片的真实物理二维空间上，看看那些表达量极低的点分布在哪里。
# 【改良 3】：弃用老旧的 SpatialFeaturePlot，改用为 Xenium 高清数据量身定制的 ImageFeaturePlot，防止跑图卡死。
ImageFeaturePlot(seurat_obj, features = "nCount_xenium", size = 0.5)

# ------------------------------------------------------------------------------
# 2. 制定方案：设定过滤阈值 (Thresholds)
# ------------------------------------------------------------------------------
# 注意：这里的数字已经针对 Xenium 5K 数据做了更科学的预设！
# 但在真实操作中，必须根据你上面画出的小提琴图的形态，来动态调整这几个数字。

min_features <- 20    # 最小基因数下限（剔除毫无活性的碎片，通常 15-25 即可）
max_features <- 2500  # 最大基因数上限（剔除双细胞。Xenium 5K 测得极深，上限要适当放宽，避免错杀好细胞）
min_counts   <- 30    # 最小RNA总分子数下限（剔除信号极弱的背景噪音点）

# ------------------------------------------------------------------------------
# 3. 执行切除：正式过滤
# ------------------------------------------------------------------------------
# 【改良 4】：弃用 subset() 函数。
# 在处理复杂对象时，subset 容易出现神经刀（找不到列名）。改用 R 最底层的方括号 [] 提取法，直接从档案库挑人，100% 成功。
seurat_obj <- seurat_obj[, seurat_obj$nFeature_xenium > min_features & 
                           seurat_obj$nFeature_xenium < max_features & 
                           seurat_obj$nCount_xenium > min_counts]

# ------------------------------------------------------------------------------
# 4. 术后复查：再次画图确认
# ------------------------------------------------------------------------------
# 重新画一次小提琴图，看看极端的高值和低值是不是已经被平滑地去掉了
VlnPlot(seurat_obj, 
        features = c("nFeature_xenium", "nCount_xenium"), 
        ncol = 2, 
        pt.size = 0) + 
  theme(legend.position = "none")

# 打印一下现在还剩多少个高质量细胞，心里有个数
print("--- 质量控制（QC）手术完成 ---")
print(paste("当前剩余高质量细胞数：", ncol(seurat_obj)))
